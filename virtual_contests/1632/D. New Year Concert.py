def sol():
    n = ipt()
    a = ilist()
    res = [0] * n
    prev = -1
    curr = 0
    st = SegmentTree(a)
    def bs(left, right):
        idx = right
        while left <= right:
            mid = left + (right - left) // 2
            gcd_upto = st.query(mid, idx + 1)
            target = idx - mid + 1

            if gcd_upto == target:
                return mid
            elif gcd_upto < target:
                left = mid + 1
            else:
                right = mid - 1

        return -1
    for r in range(n):
        l = bs(0, r)
        if l == -1 or l <= prev:
            res[r] = curr
            continue
        prev = r
        curr += 1
        res[r] = curr
    print(ljoin(res))


