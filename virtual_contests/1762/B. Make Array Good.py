MT        = True

#---------------------------------------------------
def sol():
    n = ipt()
    a = ilist()
    mn = min(a)
    res = []
    for i in range(n):
        if a[i] == mn: continue
        curr = mn
        while curr <= a[i]:
            curr *= 2
        res.append((i + 1, curr - a[i]))
    print(len(res))
    for i in res:
        print(*i)

#---------------------------------------------------
def main():
    for _ in range(ipt() if MT else 1): sol()

