def sol():
    p = ipt()
    numbers = ilist()

    tree = [[] for _ in range(p)]
    connected = [False] * p
    connected[0] = True

    def is_connected(v):
        visited = [False] * p
        stack = [0]
        visited[0] = True
        while stack:
            curr = stack.pop()
            if curr == v:
                return True
            for neighbor in tree[curr]:
                if not visited[neighbor]:
                    visited[neighbor] = True
                    stack.append(neighbor)
        return False

    for x in numbers:
        a = next(i for i in range(p) if not connected[i])
        left, right = 0, p - 1

        while left <= right:
            mid = (left + right) // 2
            v = (mid * x) % p
            if is_connected(v):
                left = mid + 1
            else:
                right = mid - 1

        k = right
        v = (k * x) % p
        u = ((k + 1) * x) % p
        tree[v].append(u)
        tree[u].append(v)
        connected[u] = True

    for i in range(p):
        for j in tree[i]:
            if i < j:
                print(i, j, (j - i) % p)

