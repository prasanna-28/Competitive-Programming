def sol():
    n, m = ivars()
    g = []
    for _ in range(m):
        g.append(ilist()[1:])

    b = ilist()
    idx = {}
    for i,v in enumerate(b):
        if v in idx: continue
        idx[v] = i
    res = [0] * (n)
    for i in g:
        for j in i:
            if j not in idx:
                break
        else:
            if len(i) == 1:
                res[idx[i.pop()]] += 1
            else:
                res[idx[max(i, key = lambda x: idx[x])]] += 1
    prev = res[0]
    print(prev)
    for i in res[1:]:
        prev += i
        print(prev)

