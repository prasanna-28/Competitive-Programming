def sol():
    n, k = ivars()
    no = ((n * (n - 1))//2) - k
    no2 = (n * (n - 1))//2
    dp = [[0] * (no2 + 1) for _ in range(n + 1)]
    prv = [[0] * (no2 + 1) for _ in range(n + 1)]
    dp[0][0] = 1

    for i in range(n + 1):
        for s in range(no2 + 1):
            if not dp[i][s]:
                continue
            for l in range(1, n - i + 1):
                add = l * (l - 1) // 2
                if s + add <= no2 and not dp[i + l][s + add]:
                    dp[i + l][s + add] = 1
                    prv[i + l][s + add] = l


    if not dp[n][no]:
        print(0)
        return
    blocks = []
    i, s = n, no
    while i > 0:
        blocks.append(prv[i][s])
        l = i
        i -= prv[i][s]
        s -= prv[l][s] * (prv[l][s] - 1) // 2
    blocks.reverse()
    cur = n
    res = []
    for l in blocks:
        start = cur - l + 1
        res.extend(range(start, cur + 1))
        cur -= l
    print(ljoin(res))

