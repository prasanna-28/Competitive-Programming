def sol():
    l, r, G = ivars()
    if G > r:
        print("-1 -1")
        return

    M = (l + G - 1) // G
    X = r // G

    if M > X:
        print("-1 -1")
        return

    if M == X:
        if M == 1 and (G >= l and G <= r):
            print(G, G)
        else:
            print("-1 -1")
        return


    best_m = -1
    best_x = -1
    best_diff = -1

    m_low  = M
    m_high = min(M + 200, X)
    x_low  = max(X - 200, M)
    x_high = X

    for m in range(m_low, m_high + 1):
        for x in range(x_high, x_low - 1, -1):
            if x <= m:
                break
            if math.gcd(m, x) == 1:
                diff = x - m
                if diff > best_diff:
                    best_diff = diff
                    best_m = m
                    best_x = x
                elif diff == best_diff and m < best_m:
                    best_m = m
                    best_x = x


    if best_diff == -1:
        print("-1 -1")
    else:
        A = G * best_m
        B = G * best_x
        print(A, B)

